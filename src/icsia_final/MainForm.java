    private String getProfit ()
    {
        double profit = 0;
        
        try{
            Statement st = myconObj.createStatement(); 
            ResultSet res = st.executeQuery("SELECT SUM(paymentAmount) FROM Cindy.clients");
            while (res.next()) {
                double c = res.getDouble(1);
                profit = profit + c;
            }
        }
        catch (SQLException e){
             e.printStackTrace ();
        }
        return df2.format(profit);
    }
    private String getTax ()
    {
        double profit = Double.parseDouble (getProfit());
        double taxRate = 0.13;
        double t = profit * taxRate;
        return df2.format(t);
    }
