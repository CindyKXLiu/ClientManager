    private int getNextID (String dbName){
        int id = 0; 
        try{
            myresObj = mystatObj. executeQuery ("Select COUNT(*) AS rowcount from Cindy." + dbName);
            myresObj.next();
            id = myresObj.getInt("rowcount"); 
            myresObj.close();
        }
        catch (SQLException e){
           e.printStackTrace();
        }
        System.out.print ("id:" + id + "   new id: " + (id+1));
        return id + 1;
    }
    
